package day1_basics;

public class DataTypesInJava {
    public static void main(String[] args) {

     /**
     * Primitive
     *  1.Integer
     *      1.a)Short
     *      1.b)byte
     *      1.c)int
     *      1.d)long
     *  2.Float-Point Numbers
     *      2.a)double
     *      2.b)float
     *  3.Characters
     *      3.a)char
     *  4.Boolean
     *      4.a)boolean
     */

     byte b = -127;
     short s = 127;
     int i = 127;
     long l = 127L;
     float f = 123.456f;
     double d = 123.456;
     boolean bool = true;
     char c = 'A';

     /**
      * Non-Primitive
      *     1.String
      *     2.Class
      *     3.Array
      *     4.etc...
      */

     String string = "Hello";

    }
}
