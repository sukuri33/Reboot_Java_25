package day1_basics;

public class RelationalOps {
    public static void main(String[] args) {
//Only returns boolean value -- True OR False
        System.out.println(1>0);//Greater than
        System.out.println(2==0);//Equal
        System.out.println(2<3);//Less than
        System.out.println(2!=3);//Not equal
        System.out.println(2<=3);//Less than OR equal
        System.out.println(2>=3);//Greater than OR equal

    }
}
