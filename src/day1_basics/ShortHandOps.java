package day1_basics;

public class ShortHandOps {
    public static void main(String[] args) {

        //Assignment
        int a = 10;
        int b = 20;
        a = b;
        System.out.println(a);
        //Addition Assignment
        int c = 10;
        int d = 20;
        c += d;//c = c + b;
        System.out.println(c);
        c *= b;//c = c * b;
        System.out.println(c);

    }
}
